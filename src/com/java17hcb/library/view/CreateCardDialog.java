    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        String fullname = tfFullname.getText();
        Date DOB = DateUtils.asDate(dpDOB.getDate());
        int type = 0;
        switch(btngType.getSelection().getActionCommand()){
            case "X":
                type = LibraryCard.Type.X;
                break;
            case "Y":
                type = LibraryCard.Type.Y;
                break;
        }
        String address = tfAddress.getText();
        String email = tfEmail.getText();
        
        int result = BusLibraryCard.getInstance().createLibraryCard(fullname, type, DOB, address, email);
        switch(result){
            case BusLibraryCard.CURRENT_STAFF_DONT_HAVE_PERMISSION:
                JOptionPane.showMessageDialog(this, 
                        "You don't have permission to create new library card. Please login with another account and try again!",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                break;
            case BusLibraryCard.CREATE_UNKNOWN_ERROR:
                JOptionPane.showMessageDialog(this, 
                                        "There is some problem when create new library card. Please try again later.",
                                        "Error",
                                        JOptionPane.ERROR_MESSAGE);
                break;
            case BusLibraryCard.CREATE_SUCCESS:
                JOptionPane.showMessageDialog(this, 
                                        "Create new library card success!",
                                        "Success",
                                        JOptionPane.INFORMATION_MESSAGE);
                break;
        }
        ((MainFrame)parent).setupCardTable();
        dispose();
    }//GEN-LAST:event_btnOKActionPerformed

